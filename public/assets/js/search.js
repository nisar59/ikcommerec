            totalamount = api
                .column( 5 )
                .data()
                .reduce( function (a, b) {
                    return intVal(a) + intVal(b);
                }, 0 );

            // Total over this page
            pageTotalQ = api
                .column( 4, { page: 'current'} )
                .data()
